let rotation = 0;
let cubePos = [0, 0, 0];

const xMin = -0.5;
const xMax = 0.5;
const yMin = -0.5;
const yMax = 0.5;

let width, height, ctx;
window.addEventListener('load', () => {
    for (const slider of document.querySelectorAll('input[type=range]')) {
        slider.value = 0;
    }
    
    const dpr = window.devicePixelRatio || 1;
    
    const canvas = document.getElementById('canvas');
    width = canvas.getBoundingClientRect().width;
    height = canvas.getBoundingClientRect().height;
    
    canvas.width = width * dpr;
    canvas.height = height * dpr;
    
    ctx = canvas.getContext('2d');
    ctx.scale(dpr, dpr);
    
    render();
});

function setRotation(amt) {
    rotation = amt;
    document.getElementById('rotation-display').innerHTML =`${Math.round(amt * (180 / Math.PI))}&deg;`;
    render();
}

function setX(x) {
    cubePos[0] = x;
    document.getElementById('x-display').innerHTML = x;
    render();
}

function setY(y) {
    cubePos[1] = y;
    document.getElementById('y-display').innerHTML = y;
    render();
}

function setZ(z) {
    cubePos[2] = z;
    document.getElementById('z-display').innerHTML = z;
    render();
}

function render() {
    ctx.clearRect(0, 0, width, height);
    
    const cos = Math.cos(rotation);
    const sin = Math.sin(rotation);
    const transformedVerts = verts.map(vert => ([
        vert[0] * cos - vert[2] * sin + cubePos[0],
        vert[1] + cubePos[1],
        vert[0] * sin + vert[2] * cos + 5 + cubePos[2],
        
    ]));
    
    let screenVerts = [];
    for (let p of transformedVerts) {
        let x = p[0] / p[2];
        let y = -p[1] / p[2];
        
        x = (x - xMin) / (xMax - xMin);
        y = (y - yMin) / (yMax - yMin);
        
        ctx.fillRect(x * width, y * height, 1, 1);
        screenVerts.push([x * width, y * height]);
    }
    
    for (let e of edges) {
        const start = screenVerts[e[0]];
        const end = screenVerts[e[1]];
        ctx.beginPath();
        ctx.moveTo(...start);
        ctx.lineTo(...end);
        ctx.stroke();
    }
};
